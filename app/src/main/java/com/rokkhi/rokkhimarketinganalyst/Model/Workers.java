package com.rokkhi.rokkhimarketinganalyst.Model;

public class Workers {

    private String fw_name;
    private String fw_nid;
    private String fw_phone;
    private String fw_uni;
    private String fw_address;
    private String fw_dob;
    private String fw_joindate;
    private String fw_mail;
    private String fw_imageUrl;


    public Workers() {
    }

    public Workers(String fw_name, String fw_nid, String fw_phone, String fw_uni, String fw_address,
                   String fw_dob, String fw_joindate, String fw_mail, String fw_imageUrl) {
        this.fw_name = fw_name;
        this.fw_nid = fw_nid;
        this.fw_phone = fw_phone;
        this.fw_uni = fw_uni;
        this.fw_address = fw_address;
        this.fw_dob = fw_dob;
        this.fw_joindate = fw_joindate;
        this.fw_mail = fw_mail;
        this.fw_imageUrl = fw_imageUrl;
    }

    public String getFw_name() {
        return fw_name;
    }

    public void setFw_name(String fw_name) {
        this.fw_name = fw_name;
    }

    public String getFw_nid() {
        return fw_nid;
    }

    public void setFw_nid(String fw_nid) {
        this.fw_nid = fw_nid;
    }

    public String getFw_phone() {
        return fw_phone;
    }

    public void setFw_phone(String fw_phone) {
        this.fw_phone = fw_phone;
    }

    public String getFw_uni() {
        return fw_uni;
    }

    public void setFw_uni(String fw_uni) {
        this.fw_uni = fw_uni;
    }

    public String getFw_address() {
        return fw_address;
    }

    public void setFw_address(String fw_address) {
        this.fw_address = fw_address;
    }

    public String getFw_dob() {
        return fw_dob;
    }

    public void setFw_dob(String fw_dob) {
        this.fw_dob = fw_dob;
    }

    public String getFw_joindate() {
        return fw_joindate;
    }

    public void setFw_joindate(String fw_joindate) {
        this.fw_joindate = fw_joindate;
    }

    public String getFw_mail() {
        return fw_mail;
    }

    public void setFw_mail(String fw_mail) {
        this.fw_mail = fw_mail;
    }

    public String getFw_imageUrl() {
        return fw_imageUrl;
    }

    public void setFw_imageUrl(String fw_imageUrl) {
        this.fw_imageUrl = fw_imageUrl;
    }
}



